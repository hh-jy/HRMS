package com.bean;

public class Emp_Salary {
    private Integer salEmpId;

    private Double basicSalary;

    private Double rewardSal;

    private Double punishSal;

    private Double empSalary;

    public Integer getSalEmpId() {
        return salEmpId;
    }

    public void setSalEmpId(Integer salEmpId) {
        this.salEmpId = salEmpId;
    }

    public Double getBasicSalary() {
        return basicSalary;
    }

    public void setBasicSalary(Double basicSalary) {
        this.basicSalary = basicSalary;
    }

    public Double getRewardSal() {
        return rewardSal;
    }

    public void setRewardSal(Double rewardSal) {
        this.rewardSal = rewardSal;
    }

    public Double getPunishSal() {
        return punishSal;
    }

    public void setPunishSal(Double punishSal) {
        this.punishSal = punishSal;
    }

    public Double getEmpSalary() {
        return empSalary;
    }

    public void setEmpSalary(Double empSalary) {
        this.empSalary = empSalary;
    }
}